package variable;

public class Var5 {
    public static void main(String[] args) {
        //1.변수 선언,초기화 작업 따로
        int a ;
        a = 1;
        System.out.println(a);

        int b = 2; //뱐수 선언과 초기화 동시에
        System.out.println(b);

        int c = 3, d = 4; //여려 변수 선언과 초기화를 한번에
        System.out.println(c);
        System.out.println(d);
    }

}